using System;
class FlatDouble
{
    static void Main(string[] args)
    {
        double numDouble;
        float numFloat;
        
        numDouble = 34.567839023;
        Console.WriteLine(numDouble);

        numFloat = 12.345f;
        Console.WriteLine(numFloat);

        numDouble = 8923.1234857;
        Console.WriteLine(numDouble);

        numFloat = 3456.091f;
        Console.WriteLine(numFloat);
    }
}
